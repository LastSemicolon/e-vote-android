public class Calon {
    private String uuid;
    private String avatar;
    private String nama;
    private String nim;

    public Calon() {}

    public String getUuid() {
        return uuid;
    }

    public String getAvatar() {
        return avatar;
    }

    public String getNama() {
        return nama;
    }

    public String getNim() {
        return nim;
    }
}
